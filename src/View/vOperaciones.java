    void actualizarTabla() {
        try {
        switch (jComboBox1.getSelectedIndex()) {
            case 0:
                    this.jTable1.setModel(this.cargarMovimientos(Movimientos.buscarMovimientos(Bannet.usuarioActivo.getId())));
                    this.jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                    jTable1.setPreferredScrollableViewportSize(jTable1.getPreferredSize());
                break;
                
            case 1:
            case 2:
            case 3:
                this.jTable1.setModel(this.cargarMovimientos(Movimientos.filtrarMovimientos(Bannet.usuarioActivo.getId(),jComboBox1.getSelectedItem().toString())));
                    this.jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                    jTable1.setPreferredScrollableViewportSize(jTable1.getPreferredSize());
                break;
        }
         } catch (SQLException ex) {
                    System.err.println("Error: " + ex.getMessage());
                }
    }

    protected DefaultTableModel cargarMovimientos(ArrayList<Movimientos> movimientos) {
        DefaultTableModel tableModel = new DefaultTableModel(this.columns, 0) {
            public boolean isCellEditable(int rowIndex, int mColIndex) {
                return false;
            }
        };

        for (Movimientos mov : movimientos) {
            Object[] o = {
                mov.getFecha(),
                "$" + formatoVista.format(mov.getCantidad()),
                mov.getTipo(),
                "$" + formatoVista.format(mov.getSaldoRes()),
                mov.getConcepto()};
            tableModel.addRow(o);
        }
        return tableModel;
    }

